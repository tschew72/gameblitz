// Helper functions
export function getWave(waveId: number): GameWave | undefined {
  return WAVES.find((w) => w.id === waveId);
}

export function getGame(waveId: number, gameId: string): GameConfig | undefined {
  const wave = getWave(waveId);
  return wave?.games.find((g) => g.id === gameId);
}
